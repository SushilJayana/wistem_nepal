        <h2>Power of Radiance is the Hope for the Awardee</h2>

        <p class="excert">
            <b><i>“Power of Radiance is the Hope for the Awardee”</i></b> says Clé de Peau Beauté Power of Radiance one
            of the Awardee
            Binita Shrestha(Chairman & Managing Director, Women in STEM Nepal).
        </p>

        <p class="excert">
            Clé de Peau Beauté Power of Radiance one of the Awardee Binita Shrestha(Chairman & Managing Director, Women
            in STEM Nepal) says <i>,” I feel that Power of Radiance is the hope for the awardee to diversify their areas
                of
                working particularly in the field of education. In my case, it’s the hope to reach out to as many girls
                as
                possible and encourage them to continue education from different places I have been working in and
                intend to
                work in future or to accelerate the rate of working in the STEAM cause, also the hope for my support
                system
                as they have been believing in my crazy instincts despite of the role models to look up to in STEAM
                fields”.</i>
        </p>

        <p class="excert">
            She further adds<i>, “Through WiSTEM Nepal organization, we will use the Power of Radiance Awards to bring
                about change in the Education system in Nepal, including the updating of teaching methods and also
                introducing new methodologies like the STEAM approach to education. For Instance “Adding Arts to STEM”,
                one
                of the coolest things we do.”</i>
        </p>

        <p class="excert">
            <i>“In order to encourage young girls to come out of their comfort zones, continue education and choose
                computing careers, arts have a major role-play. If we dig into it then science andi arts can’t be
                separated
                or they go hand in hand which is why we rely upon arts in the classroom for the innovative designs that
                are
                focused on project based learning methods to match with the latest advancement in technology fields.
                Therefore, young girls are experimenting with hardware-software and communicating with arts and design
                thinking to build their imaginations”,</i> says one of the Award Winner.
        </p>
