        <h2>Empower Her</h2>

        <p class="excert">
            <i>
                “I want my daughter to follow her dreams, be independent. My seven-year-old studies at grade one at a
                local school in Kalopul, she is smart, speaks English unlike me and loves math. I wish to get her
                educated so she can shape a better fate than mine, a different world with limitless possibilities.”
            </i>
        </p>

        <p class="excert">She is Sabitri Shrestha, born and raised in Mahottari district, Terai. As she looked back at
            her childhood, she recalled herself being shy and quiet, someone who loved dancing. Raised in a family of
            six people, where they didn’t have much, yet her parents fulfilled her needs. After her father died, the
            children had to care for the home; She was only thirteen. Soon she had to leave school and couldn’t continue
            past eight grade.</p>

        <p class="excert"><i>“At nineteen, I got married, as it was the right thing to do for a girl. They said marriage
                would provide me with security. After a year, my spouse and I shifted to Kathmandu.”</i>She says.</p>

        <p class="excert">It’s been twelve years since Sabitri didi stayed in Kathmandu. The initial days were full of
            struggles for her to adjust to an unfamiliar environment. And after she had a daughter, the family started
            having financial crises. She supported the family by working as a house help. She had no training or other
            skills at all, so she started with the only thing she knew and has always done.</p>

        <p class="excert">Since the day she started, she has helped several families with their chores, and they pay her
            accordingly. But to survive in the capital even with the salary summed up, providing the kid with her
            education, and fulfilling the basic needs is difficult.</p>

        <p class="excert">As for her husband, <i>“he is not very supportive of the family; I have a hard time doing the
                household chores, look after the daughter, work, and the support that I get from him is rare”,</i> She
            says</p>

        <p class="excert">She doesn’t have big ambitions but eventually wants to have a home to call hers. With the
            hardships she has faced, she often thinks about had she gotten the chance to study further, perhaps the
            scenario would be different. With all the struggles she does, the discrimination she faced motivates her to
            work for the better future for her daughter. To educate her so they can have a better life.</p>
