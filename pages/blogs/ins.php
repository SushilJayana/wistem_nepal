        <h2>Inspire the next generation girls with STEM Toys</h2>
        <p class="excert">
        The main problem is that middle school girls are dropping their interest in computing subjects like Science and Mathematics. Girls represent the minority in STEM fields. While 54.54% of the total women population in Nepal, about 26.17% are choosing STEM (Science, Technology, Engineering, and Mathematics). Women occupy only 19% of the total students in engineering and 7.8% of the total students in research-related jobs. However, these data are not accurate. To understand why the percentage of girls choosing STEM is declining is due to the root causes, ie the early stereotypes formed in our society, schools, and the environment we grow in. Study shows that most girls are not getting the right mentorship as required at middle schools and also they are not finding girl role models to whom they can connect to.  Society is treating our girl child differently or raised up feeling minority unintentionally in the name of culture, traditions, and others. Girls often feel the constant pressure of being perfect than brave and hardly take risks. They canâ€™t fail and if they do, they self doubt their capabilities and let other people make decisions for them. Hence, the majority of girls choose those subjects where there are more women role models.
        Part of the solution to bring young girls in STEM is to give them an experiment with STEM toys or toolkits and enroll in hands-on learning experiments with STEM. STEM stands for science, technology, engineering, and math. Through our SMART CHELI project, young girls are given STEM tools from an early age then it builds the foundation of engineering skill-sets encouraging critical thinking, increase problem-solving capabilities and design their imaginations. They will learn about failures and taking risks since STEM is all about experimenting with ideas.
