        <h2>WISTEM Project Assistantship(Only for Girls)</h2>

        <p class="excert">WISTEM Project Assistantship is a project-based assistance initiated by Women in STEM Nepal
            organization targeted to those girls who are willing to start their career in STEAM/computing fields. During
            WISTEM Assistantship, she will be responsible for overseeing/coordinating STEAM inhouse projects that
            are/will be researched, planned and executed under the direct supervision of the project directors. The
            project assistant oversees all aspects of a project as per assigned that are duly attended to and completed
            at the right time, regardless of challenges that may be encountered.</p>


        <!-- <img class="img-fluid" src="./img/lean-in/blogs/wd_port/wd_cycle.jpg" /> -->

        <h4>Roles and Responsibilities</h4>
        <ul>
            <li>She will be handling details related to conducting research, drafting reports, keeping project
                management tracking systems updated and fielding calls and managing correspondence from project team
                members and report to the project director.
            </li>
            <li>Developing an overview of the approach, assigning team members to various responsibilities, setting
                goals and developing timelines and ensuring all elements of a project are progressing on time and on
                budget as anticipated.
            </li>
            <li>She will carry out administrative duties by collecting data, sorting, filing and sending out projects.
            </li>
            <li>She should be flexible in working offline and online as per project and situation requirements.</li>
        </ul>

        <h4>Thematic Areas working on</h4>
        <ul>
            <li>Policy</li>
            <li>Gender</li>
            <li>STEAM and Education</li>
            <li>Women in computing</li>
            <li>Patriarchy and Social taboos</li>
            <li>Technology Innovation and Social enterprise</li>
            <li>Women and Data</li>
            <li>Mindfulness</li>
            <li>Soft skills and Self Improvement</li>
            <li>Entrepreneurship</li>
        </ul>

        <h4>Eligibility Criterias</h4>
        <ul>
            <li>She must have completed her highschool degree or enrolled in an undergraduate degree currently.</li>
            <li>Should be between the age group of (20-25) years.</li>
            <li>Must be a Nepali citizen.</li>
            <li>Well spoken in Nepali and English language.</li>
            <li>Having management skills will be an added value to your cv.</li>
            <li>Apply only if you have some knowledge in education/STEAM/technology or are enthusiastic about computing
                fields.
            </li>
        </ul>

        <h4>Minimum Stipend</h4>
        <ul>
            <li>The minimum stipend is Rs 12000 per month but no bars for deserving candidates.</li>
        </ul>

        <h4>How to Apply</h4>

        <p class="excert">
            If you find yourself fit into above eligibility criterias and enthusiastic about working the listed thematic
            areas, then follow these steps to apply for the WISTEM Project Assistantship today.
        </p>
        <ul>
            <li>Write a cover letter explaining why you are willing to join us along with your CV and send it to <a
                    href="mailto: hr@wistemnepal.org">hr@wistemnepal.org</a><i>(Please only include the relevant
                    information in your cover letter and CV)</i></li>
            <li>Also, include five thematic areas in your cover letter which you excited working on as per your interest
                and expertise.
            </li>
        </ul>

        <p class="excert"><b><u>Deadline</u>:&nbsp</b>Apply by August 15th, 2020.</p>

        <p class="excert"><b><u>Selection and Interview Deadline</u>:&nbsp</b>August 20th, 2020..</p>

        <p class="excert"><b><u>Joining Team WISTEM</u>:&nbsp</b>August 25th, 2020.</p>

