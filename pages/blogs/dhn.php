        <h2>Dashain and 2020</h2>
        <p class="excert">Dashain is the most prominent festival in the Hindu community, celebrated all over Nepal. It
            starts at Ashwin Shukla Pratipada, followed by Navaratri, then the primary day being the Dashami and ends on
            Purnima the full moon day. The 1st day is called Ghatasthapana, the 7th as Fulpati or Saptami, 8th day as
            Asthami, 9th as Navami and 10th day the Bijaya Dasmani are the most auspicious days.</p>
        <p class="excert">According to Puran in Satya Yuga, there was an Asur named Mahisasur. With the grace of Lord
            Brahma, Mahisasur got a boon that none can kill him except a female. Thus the arrogance overruled Mahisasur,
            and he started obstructing Devlok. To stop Mahisasur, Trisakti created a goddess called Navadurga. She
            battles with Mahisasur, fought for nine days with her several forms each one day starting with Shailaputri,
            Brahmacharini, Chandraghanta, Kushmanda, Skandamata, Katyayani, Kaalratri, Mahagauri and Siddhidhatri and
            finally killed Mahisasur on Navami. Thus the victory is celebrated on the 10th day as Bijaya Dashami.</p>
        <p class="excert">Another story that adds to the festival is that God Ram in battle with Ravan worshipped Devi
            for nine days in Navratri and finally killed Ravan in Navami evening, thus Dashain is celebrated as the day
            of victory of good over evil.</p>
        <p class="excert">If you have heard it, People celebrated Dashain in Chaitra, but because of the excessive heat,
            it was shifted to Ashoj, when the climate is moderate.</p>
        <p class="excert">People celebrate Dashain with a lot of enthusiasm and excitement in the country. From kids to
            older ones, the people get enormously happy, perhaps some reasons being a month-long holiday for the kids,
            kites, food and swings. On the Dashami, the younger ones put tika from the older ones, take blessings with a
            specific mantra. People travel to relatives’ places, to put the tika and get graces. The gathering gets
            heartwarming, relatives who haven’t met in a year-long, attempt to come to your house, communicate and
            participate in celebrations. </p>
        <p class="excert">The festival is pretty diligent for people, visiting temples, shopping or the people
            purchasing goats and all. There’s a big market that sells these animals on Dashain, for slaughtering them as
            an offering to please Devi. With that, Dashain also gets criticised for the mass slaughter, and it’s a
            controversy on its own. Nevertheless, it’s the biggest celebration in the nation.</p>
        <p class="excert">However, the celebration might not correspond this year. Amid pandemic, the virus has affected
            everyone. With the accelerating numbers of infected people in the community, it’s our responsibility to keep
            us and the loved one’s safe. Let’s follow the social distancing strictly and avoid travelling and meeting
            relatives, to be secure.</p>
        <p class="excert">Perhaps the social distancing can be taken to next-level and celebrate virtually. This year,
            let’s change the pattern, one of the biggest festivals in the country, let’s adopt diverse approaches.
            Gather the family members, get into social media, connect to everyone through virtual calls and celebrate
            with each one that matters. And just like Devi Durga got victory over Mahisasur, we shall fight against this
            virus together staying at home and win our freedom back.</p>
        <p class="excert">At last, we wish you a delightful Dashain from our entire team. Take precautions and enjoy the
            Dashain. Stay home, stay safe.</p>
