        <h2>Top 8 Practices for better mental health</h2>

        <p class="excert">Mental health is one of the most discussed words on the internet, yet most ignored in the
            community. Anxiety, depression, mood swings and other crucial issues, we often overlook the situations and
            people that require help.</p>

        <p class="excert">Feeling ashamed of admitting the symptoms, trying to hide it rather than acknowledging it and
            eventually end up in different tragic situations like suicide.</p>

        <p class="excert">Based on police data and projections, the World Health Organisation put Nepal as the country
            with the 7th highest suicide rate in the world (24.9 per 100,000). Public health experts say that suicide is
            the symptom, and that the actual disease is poor mental health.</p>

        <p class="excert">In 2009, the Nepalese Family Health Division’s Maternal Mortality and Morbidity Study
            published the “shocking finding” that suicide was the leading cause of death for women of reproductive age
            (15-49).</p>

        <p class="excert">Nepal has a relatively high ratio of female: male suicides (30.1/100,000 for men, 20.0/100,000
            for women) and stands out as being ranked 17th for male suicide rates but 3rd for female suicide rates.
            While male suicide rates are higher, an estimation that Nepalese women attempt to suicide three times more
            than men do. However, the numbers improved till 2016 having a ratio of female: male suicides being 1.43, yet
            thousands of lives are at risk.</p>

        <p class="excert">Suicide is one of the major reasons as neglected or unaddressed mental health problems. But
            today we aren’t talking about the problems. We discuss the solutions to improve our mental state, diverting
            our emotional response from stress and strain, to peace and contentment.</p>

        <p class="exert">let’s begin.</p>

        <h4>Meditation and exercise</h4>

        <p class="excert">Meditation is a practice where an individual uses a technique–such as mindfulness or focusing
            the mind on a particular object, thought, or activity–to train attention and awareness, and achieve a
            mentally clear and emotionally calm and stable state.</p>

        <p class="excert">An hour of silence before starting my day has proved to be the most comforting thing. And I
            can guarantee that it can work miracles on you. If you are a beginner, start with 5 minutes a day, then 10
            and 15 and so on. And if you are already on the path to enlightenment, then you know what I am talking
            about.</p>

        <p class="excert">Then comes the exercise.</p>

        <p class="excert">Exercise develops your physical and mental state. Any form of exercise, starting from yoga,
            joining gyms, boxing, Zumba, swimming, jogging or anything that requires the movement of the body parts is
            sufficient.</p>

        <p class="excert">Some time for yourself after waking up, some meditation and movement of the body are effective
            for your mental state.</p>

        <p class="excert">So why not start now?</p>

        <h4>Travelling/ exploring </h4>

        <p class="excert">Travelling is one of the most rational things you could do for yourself. Going to places,
            connecting to nature, exploring the diversity and cultures, travelling provides people with a unique
            prospect.</p>

        <p class="excert">One feels rejuvenated, and it heals the stress. It improves your patience for uncertainty, if
            you haven’t explored yourself, travelling will make it happen. You get to meet a new you throughout the
            experiences.</p>

        <p class="excert">Go for frequent road trips, two hours in every couple of days, gather friends and plan a long
            trip, cancel it hundreds of times but at least make it happen. Travel the world, explore as much as you can,
            if nothing travelling will keep you sane.</p>


        <h4>Practising gratitude and self-acceptance</h4>

        <p class="excert">Learn gratitude. Be thankful to everyone and everything around. Parents for providing you with
            love, affection and care. Friends and relatives for being there in your life. Don’t complain, as nature has
            provided you with the right thing needed for your growth. Embrace it.</p>

        <p class="excert">And once you finish appreciating others, look at yourself in the mirror. Show some gratitude
            towards yourself. All your flaws and imperfections, accept it. Make peace with your past. Try to see the
            glass half full, your strengths, skills and your beautiful smile. Stay proud of who you are, be grateful for
            it.</p>

        <p class="excert">If you won’t love you, no one will. Which will eventually disturb you and influence your
            mental well-being. So say it aloud, Dear God, that you for everything. And to yourself, that you are amazing
            in your own quirky way.</p>

        <h4>Reach out to people</h4>

        <p class="excert">Always reach out to people. The close ones that understand you, those care for you and admire
            you. Reach out to someone if you feel anything is not right. When you feel depressed, anxious, defeated,
            angry or anything, talking to people helps. And if that doesn’t, taking an expert’s help will work.</p>

        <p class="excert">Some people are willing to help you, genuinely worry about you. If you are uncomfortable
            talking to people you know, seek help from strangers, take therapy, talk to the doctors. They can help you
            the way you need. Shying away from talking about how you feel is not the solution, but reaching out to
            people is.</p>


        <h4>Learn to say NO</h4>

        <p class="excert">Our inability to say NO involves us in uncomfortable or severe situations. And that bothers us
            emotionally. So, learning to say no helps a lot through personal or professional life.</p>

        <p class="excert">We should learn to walk off the situations that keep us distressed or if we don’t fit in.
            Involving or accepting situations or people to please others is rather harmful to your mental state.</p>

        <p class="excert">So, walk away and say NO to anything you don’t want, to eat, to go to the place, to do
            anything that you are unsure of. And see how good that feels.</p>


        <h4>Drink enough water/ healthy eating practices</h4>

        <p class="excert">Dehydration affects your mood, reduces your cognitive and motor skills, makes you more
            sensitive to pain, also affects your memory. So, drinking a required amount of water daily is beneficial to
            not only your skin and kidney but the brain. Start with two glasses of warm water every morning.</p>

        <p class="excert">Again, drinking water alone isn’t enough. Healthy eating practices are equally important.
            There’s a theory that says you become what you eat, and that’s true. Your eating pattern and the
            constituents you consume affects your mind and body. Fasting also can boost brain function and prevent
            neurodegenerative disorders.</p>

        <p class="excert">So, drinking enough water and eating healthy affects your mind and mental state for the
            better.</p>

        <h4>Read books and write a journal</h4>

        <p class="excert">Books are the ultimate ways to escape reality. Books provide you with hope, give you different
            perceptions and an understanding. It can help you gain confidence, battle with your inner demons and win at
            life. Reading at least 10 pages a day helps you calm, stimulates your brain.</p>

        <p class="excert">And write a journal of your day. Be honest while scribbling your thoughts into the paper. Once
            you complete it, read it out loud. You will see most of the things you worry about don’t even make sense.
            Also, reading these journals helps you with an insight into yourself, analyze them and act accordingly.</p>

        <p class="excert">Read some pages every day before settling yourself to bed and write a journal of your day or
            something specific that has affected you throughout the day. Swear, that helps.</p>

        <h4>Assign a “me time”</h4>

        <p class="excert">Get a hobby, keep yourself engaged. Get creative. Cook yourself some good meals, sketch, sing,
            dance, get weird, do anything that you love. Try new things, new hobbies to explore, something you’ve never
            done before. Do what makes you happy.</p>

        <p class="excert">Some time for only you, focus on your development, have some self-reflection and release
            anything that causes tension.</p>

        <p class="excert">Times can be tough, things mightn’t go according to the plan and situations to make us feel
            worthless. But one thing to keep in mind is that time changes and life will come up with better plans for
            you. Keep your heads up and smile and remember that you are doing just fine.</p>

