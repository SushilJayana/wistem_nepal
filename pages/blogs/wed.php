        <h2>A joint partnership for the Women Entrepreneurship Data project announced</h2>
        <p class="excert">

        [March 8, 2020], Women in STEM Nepal, Open Knowledge Nepal and Women Leaders in Technology signed a joint project partnership agreement which aims to make the data related to women entrepreneurs of Nepal easily accessible such that it supports new entrepreneurs and other existing stakeholders with resources for evidence-based decision making. This will eliminate the costly need for market analysis. The project also aims to highlight the current scenario of women entrepreneurs in Nepal through research and support. The project is conceptualized to raise funding up-to USD 1 million and reinvest it into various project related to data related to women and entrepreneurship in Nepal; which includes the harvesting and presentation of the existing data, development of new conceptual framework for the research, conduction, and implementation of the research project, partnership with diverse stakeholders, dissemination of the research analysis, and provision of stable support to the stakeholder working on women entrepreneurship of Nepal.

<br/>
<br/>
        <strong>Partnership Objectives</strong>
        <br/>
        <ul class="order-list">
<li>Identify short, medium, and long term goals, activities and support of the Women Entrepreneurship Data (WEData) project.</li>
<li>Develop an enthusiastic taskforce to harvest the existing data related to women entrepreneurs and present them interactively in a web portal making the finding publicly accessible.</li>
<li>Identify priority needs via feasibility analysis and develop concepts based on it.</li>
<li>Inhouse development of project material for the presentation and promotions.</li>
<li>Based on the feasibility & existing data analysis, develop methodology/framework for in-depth research on the subject.</li>
<li>Develop the web portal to disseminate and present all the findings related to women entrepreneurship and make it accessible to the general public.</li>
<li>Secure initial partnerships with start-ups, federations, civil societies, etc and sign an agreement to collaboratively conduct research.</li>
<li>Prepare the fully-fledged concept note stating methodology, survey questionnaires, and dissemination strategy.</li>
<li>Engage with all possible donors and funders, to develop and test project financing mechanisms to secure financial support.</li>
<li>Conduct the planned research with the help of stakeholders and civil societies.</li>
<li>Prepare the report and disseminate the data and analysis of the survey via the portal for further reuse.</li>
<li>Bring out narratives of need, success & failures and present it openly asking others to join in momentum to encourage the ecosystem that fosters women entrepreneurship in Nepal.</li>
<li>Build the sustainability and support mechanism for the women entrepreneurs and policymakers; who are in search of data and information.</li>
</ul>
