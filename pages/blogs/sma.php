        <h2>Smart Cheli Initiatives taking shape at public schools in Nepal</h2>
        <p class="excert">
SMART CHELI is a project that is initiated to reach out to public schools in Nepal. We aim to connect education with STEM hands-on learning experiences to the age group of 10-16 years in coding, design thinking, and electronics in 20 public schools and 1000 girls in the next 2 years. We aim to decrease the prevailing gender gap in STEM and encourage young girls to critical thinking, designing their imaginations and problem solving and get exposure to STEM from an early age. 
Do you want us to conduct the next STEM workshop at your school? 
<br/>Write to us at <b>smartcheli@wistemnepal.org</b> mentioning your school name, the flexible dates for school and your bio or role in the event.
