        <h2>Adapting with the changing times: A way forward for WEData Nepal</h2>

        <p class="excert">
            Women’s role inside the family is largely evolving in recent decades, with the increase in education, and
            the need for income in families to sustain the growing inflation. But women are not equally participative,
            due to socio-cultural and economic setbacks. There is a very difficult situation for women entrepreneurs to
            start a new business, as the resources are not easily available for market research. At the same time, the
            recurring crises which happened in Nepal over the past decade make women’s ventures take the backseat in the
            entrepreneurial race as women are bound to take the role of caretakers and givers, more often rather than
            giving priorities for self-decision making, articulateness, and opinion formation.
        </p>

        <p class="excert">To understand/highlight the current scenario via ground research and help women entrepreneurs
            to foster in the market of Nepal by the effective use of data and technology, on 08 March, 2020, we
            announced Women Entrepreneurship Data (WEData) Nepal project. But due to the challenge brought by the
            COVID-19 pandemic, we the team of Women Entrepreneurship Data (WEData) Nepal have decided to make some
            modification on our predetermined project activities by postponing the development and conduction of the
            comprehensive research for the certain time. We are going to address the post pandemic challenges created by
            COVID-19 for women entrepreneurs by opening up for data and resources so that they can tap into opportunity
            based on data-driven decision making and train them to reuse those resources to uplift the activities in the
            respective region.</p>

        <p class="excert">The updated project activities include the collection of all publicly available data on women
            entrepreneurship and policies, preparation of a report of findings stating the current status for women
            entrepreneurship (in terms of product/services and operating domain), underlying policies, and the factors
            affecting those, development of a web portal to disseminate these findings and data publically in an
            interactive fashion, hosting online/in-person boot camps and events to use the tools and resources created
            to uplift the activities in the region and releasing the results to stakeholders and to a general audience
            through multi-tiered online meetings, public announcements using social media and the internet, encouraging
            the data and evidence-based decision making for policies, and resource allocation.</p>

        <strong>
            <h4>Project Design and Activities</h4>
        </strong>
        <br/>
        <img class="img-fluid" src="./img/lean-in/blogs/wd_port/wd_cycle.jpg"/>
        <br/>
        <ol>
            <li>Collect all publicly available data on women entrepreneurship and policies.
                <ol type="a">
                    <li>List all existing policies and put together the data on entrepreneurship and economic
                        engagement.
                    </li>
                    <li>Study them along with the context of women entrepreneurship.</li>
                    <li>Prepare a data questionnaire for the development of narratives and visualizations.</li>
                    <li>Compare the past and existing status of policies and their effect on the economy and women
                        entrepreneurship.
                    </li>
                    <li>List all the stakeholders who will be affected by these policies, categorized according to their
                        areas of involvement, sectors, types of entrepreneurship, geographic locations, etc.
                    </li>
                </ol>
            </li>
            <li>Prepare a report of findings stating the current status for women entrepreneurship (in terms of
                product/services and operating domain), underlying policies, and the factors affecting those.
                <ol type="a">
                    <li>Put together different findings out of the study, etc. if there has been any correlation between
                        policies reformation and data.
                    </li>
                    <li>Prepare narratives and visualizations.</li>
                    <li>Create infographics about the findings for easier broadcast through online/social media.</li>
                    <li>List evident problems and possible solutions to those.</li>
                </ol>
            </li>
            <li>Develop a web portal to disseminate these findings and data publically in an interactive fashion.
                <ol type="a">
                    <li>Build a website to interactively visualize and publish the data.</li>
                    <li>nclude infographics, tools and other resources which can be helpful for entrepreneurs to boost
                        business.
                    </li>
                </ol>
            </li>

            <li>Organize bootcamps and events to use the tools and resources created to uplift the activities in the
                region.
                <ol type="a">
                    <li>Run tech adaptability online/offline workshops among local business owners and self employers.
                    </li>
                    <li>Push the businesses into technology for better reception.</li>
                    <li>Verify the result of the workshops.</li>
                </ol>
            </li>

            <li>Release the results to stakeholders and the general audience through multi-tiered online meetings,
                public announcements using social media and the internet, encouraging the data and evidence-based
                decision making for policies, and resource allocation.
                <ol type="a">
                    <li>Have meetings with local organizations, communities, and public service offices in the region to
                        deliver the findings, and help create smaller and local efforts on solving them locally.
                    </li>
                    <li>Report the project to the local government and donor organizations overlooking the resource and
                        funding management for local and regional entrepreneurs.
                    </li>
                    <li>Announce the findings to the public through media channels.</li>
                </ol>
            </li>
        </ol>
